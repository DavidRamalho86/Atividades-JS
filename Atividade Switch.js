let estacaoDoAno = 'inverno' ;

switch (estacaoDoAno) {
		case 'verão':
    		console.log ('Estamos no verão!');
        break;
    case 'inverno' :
    		console.log ('Estamos no inverno!');
        break ;
    case 'outono' :
    		console.log ('Estamos no outono!');
        break ;
    case 'primavera' :
    		console.log ('Estamos no primavera!');
        break ;
    default:
    		console.log ('Esta estação do ano não existe!');
        break
}