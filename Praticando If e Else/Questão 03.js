//3- Pergunta sobre Dia da Semana:
let dia = prompt("Digite um número de 1 a 7 para representar um dia da semana:");

if (dia == 1) {
  console.log("Domingo");
} else if (dia == 2) {
  console.log("Segunda-feira");
} else if (dia == 3) {
  console.log("Terça-feira");
} else if (dia == 4) {
  console.log("Quarta-feira");
} else if (dia == 5) {
  console.log("Quinta-feira");
} else if (dia == 6) {
  console.log("Sexta-feira");
} else if (dia == 7) {
  console.log("Sábado");
} else {
  console.log("Número inválido. Digite um número de 1 a 7.");
}
