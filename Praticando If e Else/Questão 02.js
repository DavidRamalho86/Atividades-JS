let num1 = prompt("Digite o primeiro número:");
let num2 = prompt("Digite o segundo número:");

if (num1 > num2) {
  console.log("O primeiro número é maior");
} else if (num1 === num2) {
  console.log("Os números são iguais");
} else {
  console.log("O segundo número é maior");
}
